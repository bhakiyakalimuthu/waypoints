package com.spring.waypoints.model;

public class CategoryDataPoints {
    private float duration;
    private float distance;
    private float total_duration;
    private float total_distance;

    public float getDuration() {
        return duration;
    }

    public void setDuration(float duration) {
        this.duration = duration;
    }

    public float getDistance() {
        return distance;
    }

    public void setDistance(float distance) {
        this.distance = distance;
    }

    public float getTotal_duration() {
        return total_duration;
    }

    public void setTotal_duration(float total_duration) {
        this.total_duration = total_duration;
    }

    public float getTotal_distance() {
        return total_distance;
    }

    public void setTotal_distance(float total_distance) {
        this.total_distance = total_distance;
    }
}
